package bbs;

public class Bbs {
	
	private int bbsID;
	private String bbsTitle;
	private String bbsDate;
	private String bbsContent;
	private int bbsAvailable;
	private String bbsName;
	private String bbsTel;
	private String bbsEmail;
	private String bbsPassword;
	
	public String getBbsName() {
		return bbsName;
	}
	public void setBbsName(String bbsName) {
		this.bbsName = bbsName;
	}
	public String getBbsEmail() {
		return bbsEmail;
	}
	public void setBbsEmail(String bbsEmail) {
		this.bbsEmail = bbsEmail;
	}
	public String getBbsTel() {
		return bbsTel;
	}
	public void setBbsTel(String bbsTel) {
		this.bbsTel = bbsTel;
	}
	public String getBbsPassword() {
		return bbsPassword;
	}
	public void setBbsPassword(String bbsPassword) {
		this.bbsPassword = bbsPassword;
	}
	public int getBbsID() {
		return bbsID;
	}
	public void setBbsID(int bbsID) {
		this.bbsID = bbsID;
	}
	public String getBbsTitle() {
		return bbsTitle;
	}
	public void setBbsTitle(String bbsTitle) {
		this.bbsTitle = bbsTitle;
	}
	public String getBbsDate() {
		return bbsDate;
	}
	public void setBbsDate(String bbsDate) {
		this.bbsDate = bbsDate;
	}
	public String getBbsContent() {
		return bbsContent;
	}
	public void setBbsContent(String bbsContent) {
		this.bbsContent = bbsContent;
	}
	public int getBbsAvailable() {
		return bbsAvailable;
	}
	public void setBbsAvailable(int bbsAvailable) {
		this.bbsAvailable = bbsAvailable;
	}
}
